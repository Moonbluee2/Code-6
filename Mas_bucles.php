<?php
// Bucle do-while que imprime los números del 1 al 5
$numero = 1;

echo "Bucle do-while (1 al 5):<br>";
do {
    echo $numero . "<br>";
    $numero++;
} while ($numero <= 5);

echo "<br>";

// Bucle while que cuenta hacia atrás desde 5 hasta 1
$contador = 5;

echo "Bucle while (5 al 1):<br>";
while ($contador >= 1) {
    echo $contador . "<br>";
    $contador--;
}
?>


